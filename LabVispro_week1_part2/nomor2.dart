void main() {
  const int uangPerHari = 5000; // Jumlah uang yang dimasukkan setiap hari
  const int jumlahHari = 30; // Jumlah hari

  // Menghitung total uang setelah 30 hari
  int totalUang = uangPerHari * jumlahHari; // Mengalikan uang per hari dengan jumlah hari

  // Menampilkan hasil
  print("==============================================");
  print("|          Total Uang di Celengan           |");
  print("==============================================");
  print("Total uang setelah 30 hari: Rp $totalUang"); // Menampilkan total uang
  print("==============================================");
}
